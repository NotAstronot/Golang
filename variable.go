package main

import "fmt"

func main() {

	name := "Feri Riski"
	fmt.Println(name)

	name = "Muh Feri Riski "
	fmt.Println(name)

	// Multiple Variable
	var (
		firstName  = "Muh"
		middleName = "Feri"
		lastName   = "Riski"
	)

	fmt.Println(firstName)
	fmt.Println(middleName)
	fmt.Println(lastName)

}
